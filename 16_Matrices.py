#Crear matrices de una forma muy facil

#Crear matrices de n dimensiones

#Crear una lista que almacenara dos listas mas

columna_a = [10,20]
columna_b = [30,40]

matriz = [columna_a,columna_b] #2x2
print(matriz)

#Obtener el primer elemento

#   matriz[x][y]
#   matriz[fila] [columna]
print(matriz[0][0])

print(matriz[0][1])

print(matriz[1][1])

#Otro ejemplo de columnas

columna_a = [10,20,30,40]
columna_b = [50,60,70,80]

matriz = [columna_a,columna_b] #2x4
print(matriz[1][3])